#!/usr/bin/env node
/**
 * Multirotor aircraft generator — emits JSBSim aircraft for tricopter,
 * hexacopter, and octocopter frames, derived from the bundled DJI F450
 * quadcopter (same DJI_E305 motors + DJI_9450 props, same rate-PID FCS
 * architecture, generalized N-motor mixer).
 *
 * Conventions (JSBSim structural frame): +x aft, +y right, +z up.
 * Motor angle θ is measured from the nose, clockwise viewed from above:
 *   front  = -x = L·cosθ,  right = +y = L·sinθ.
 * Mixer gains per motor: pitch = cosθ·√2, roll = -sinθ·√2, yaw = sense
 * (√2 keeps X-quad authority identical to the original F450 ±1 mixer).
 * Sense alternates with angle order (adjacent motors counter-rotate) —
 * even motor counts cancel torque; the tricopter instead gets a sideways
 * tail rotor for yaw (a documented simplification of the tilt-servo
 * mechanism real tricopters use, which JSBSim cannot articulate).
 *
 * Usage: node scripts/generate-multirotor.mjs [outDir]
 *   outDir defaults to public/aircraft
 */

import { mkdirSync, writeFileSync, readFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outRoot = process.argv[2] ?? join(root, 'public', 'aircraft');
const f450Dir = join(root, 'public', 'aircraft', 'F450');

const DEG = Math.PI / 180;
const SQRT2 = Math.SQRT2;

// ---------------------------------------------------------------------------
// Frame definitions
// ---------------------------------------------------------------------------

const FRAMES = [
  {
    name: 'Tri450',
    title: 'Tricopter 450 (yaw via tail rotor)',
    arm: 0.25,
    massKg: 1.2, ixx: 0.016, iyy: 0.016, izz: 0.022,
    motorAngles: [60, -60, 180],
    // Yaw handled entirely by the tail rotor; lift motors get no yaw mixing.
    liftYawMix: false,
    tailRotor: { x: 0.30, bias: 0.5, gain: -0.6 },
  },
  {
    name: 'Hexa550',
    title: 'Hexacopter 550',
    arm: 0.275,
    massKg: 2.2, ixx: 0.045, iyy: 0.045, izz: 0.080,
    motorAngles: [30, 90, 150, 210, 270, 330],
    liftYawMix: true,
    tailRotor: null,
  },
  {
    name: 'Octo650',
    title: 'Octocopter 650',
    arm: 0.325,
    massKg: 3.0, ixx: 0.090, iyy: 0.090, izz: 0.160,
    motorAngles: [22.5, 67.5, 112.5, 157.5, 202.5, 247.5, 292.5, 337.5],
    liftYawMix: true,
    tailRotor: null,
  },
];

// ---------------------------------------------------------------------------
// Geometry helpers
// ---------------------------------------------------------------------------

function motors(frame) {
  // Sort by angle (normalized 0-360) so sense alternation is geometric.
  const sorted = [...frame.motorAngles]
    .map(a => ((a % 360) + 360) % 360)
    .sort((a, b) => a - b);
  return sorted.map((angle, i) => {
    const rad = angle * DEG;
    return {
      id: `M${i}`,
      angle,
      x: -(frame.arm * Math.cos(rad)),   // structural: front is -x
      y: frame.arm * Math.sin(rad),
      sense: i % 2 === 0 ? 1 : -1,
      pitchGain: round(Math.cos(rad) * SQRT2),
      rollGain: round(-Math.sin(rad) * SQRT2),
    };
  });
}

function round(v) {
  const r = Math.round(v * 10000) / 10000;
  return Object.is(r, -0) ? 0 : r;
}

// ---------------------------------------------------------------------------
// XML emitters
// ---------------------------------------------------------------------------

function mainXml(frame) {
  return `<?xml version="1.0" ?>

<fdm_config name="${frame.name}" release="ALPHA" version="3.0">
    <!-- ${frame.title} — generated by scripts/generate-multirotor.mjs. -->
    <fileheader/>

    <metrics file="Metrics.xml"/>
    <mass_balance file="Mass.xml"/>
    <ground_reactions file="Gear.xml"/>
    <propulsion file="Propulsion.xml"/>
    <system file="Effectors.xml"/>
    <flight_control file="FlightControl.xml"/>
    <system file="SensorImu.xml"/>
    <system file="SensorBaro.xml"/>
    <system file="SensorGps.xml"/>
    <aerodynamics file="Aero.xml"/>

</fdm_config>
`;
}

function massXml(frame) {
  return `<?xml version="1.0" ?>
<mass_balance>
  <emptywt unit="KG">${frame.massKg}</emptywt>
  <location name="CG" unit="M">
    <x>0.0</x>
    <y>0.0</y>
    <z>0.0</z>
  </location>
  <ixx unit="KG*M2">${frame.ixx}</ixx>
  <iyy unit="KG*M2">${frame.iyy}</iyy>
  <izz unit="KG*M2">${frame.izz}</izz>
  <ixy unit="KG*M2">0.0</ixy>
  <ixz unit="KG*M2">0.0</ixz>
  <iyz unit="KG*M2">0.0</iyz>
</mass_balance>
`;
}

function gearXml(frame) {
  const z = -0.1524;
  const l = frame.arm * 0.6;
  const contact = (name, x, y) => `\t<contact name="${name}" type="BOGEY">
\t\t<location unit="M">
\t\t\t<x> ${round(x)} </x>
\t\t\t<y> ${round(y)} </y>
\t\t\t<z> ${z} </z>
\t\t</location>
\t\t<static_friction>0.8</static_friction>
\t\t<dynamic_friction>0.5</dynamic_friction>
\t\t<rolling_friction>0.02</rolling_friction>
\t\t<spring_coeff unit="N/M"> ${Math.round(3000 * frame.massKg / 1.4)} </spring_coeff>
\t\t<damping_coeff unit="N/M/SEC"> ${Math.round(100 * frame.massKg / 1.4)} </damping_coeff>
\t</contact>`;
  // Same trick as the F450: a front-center point avoids JSBSim's unstable
  // 4-point ground trim.
  return `<?xml version="1.0" ?>
<ground_reactions>
${contact('Front_Center', -l, 0)}

${contact('Aft_Left', l, -l)}

${contact('Aft_Right', l, l)}
</ground_reactions>
`;
}

function propulsionXml(frame) {
  const ms = motors(frame);
  const engine = (name, x, y, orientPitch, orientYaw, sense) => `  <engine file="DJI_E305" name="${name}">
    <thruster file="DJI_9450">
      <location unit="M">
        <x>${round(x)}</x>
        <y>${round(y)}</y>
        <z>0.025</z>
      </location>
      <orient unit="DEG">
        <roll>0</roll>
        <pitch>${orientPitch}</pitch>
        <yaw>${orientYaw}</yaw>
      </orient>
      <sense>${sense}.0</sense>
      <p_factor>0.0</p_factor>
    </thruster>
  </engine>`;

  const parts = ms.map(m => engine(`motor ${m.id} (${m.angle} deg)`, m.x, m.y, 90, 0, m.sense));
  if (frame.tailRotor) {
    // Sideways tail rotor: thrust along +y (blows right → nose-left moment).
    parts.push(engine('tail yaw rotor', frame.tailRotor.x, 0, 0, 90, 1));
  }
  return `<?xml version="1.0" ?>

<propulsion>
${parts.join('\n\n')}
</propulsion>
`;
}

function effectorsXml(frame) {
  const ms = motors(frame);
  const act = (name, input, index) => `    <actuator name="${name}">
      <input>${input}</input>
      <clipto>
        <min>0.0</min>
        <max>1.0</max>
      </clipto>
      <output>fcs/throttle-pos-norm[${index}]</output>
    </actuator>`;
  const parts = ms.map((m, i) => act(`ESC ${m.id}`, `fcs/cmdEsc${m.id}_nd`, i));
  if (frame.tailRotor) {
    parts.push(act('ESC tail', 'fcs/cmdEscTail_nd', ms.length));
  }
  return `<?xml version="1.0" ?>

<system name="Effectors">
  <channel name="Electronic Speed Controllers">
${parts.join('\n\n')}
  </channel>
</system>
`;
}

function flightControlXml(frame) {
  const ms = motors(frame);

  const mixer = ms.map(m => {
    const yawGain = frame.liftYawMix ? m.sense : 0;
    return `    <pure_gain name="cmdHeave_2_${m.id}">
      <input>fcs/throttle-cmd-norm</input>
      <gain>1.0</gain>
      <output>fcs/cmdHeave_2_${m.id}_nd</output>
    </pure_gain>
    <pure_gain name="cmdRoll_2_${m.id}">
      <input>fcs/cmdRoll_rps</input>
      <gain>${m.rollGain}</gain>
      <output>fcs/cmdRoll_2_${m.id}_nd</output>
    </pure_gain>
    <pure_gain name="cmdPitch_2_${m.id}">
      <input>fcs/cmdPitch_rps</input>
      <gain>${m.pitchGain}</gain>
      <output>fcs/cmdPitch_2_${m.id}_nd</output>
    </pure_gain>
    <pure_gain name="cmdYaw_2_${m.id}">
      <input>fcs/cmdYaw_rps</input>
      <gain>${yawGain}</gain>
      <output>fcs/cmdYaw_2_${m.id}_nd</output>
    </pure_gain>
    <summer name="cmd${m.id}_nd">
      <input>fcs/cmdHeave_2_${m.id}_nd</input>
      <input>fcs/cmdRoll_2_${m.id}_nd</input>
      <input>fcs/cmdPitch_2_${m.id}_nd</input>
      <input>fcs/cmdYaw_2_${m.id}_nd</input>
      <output>fcs/cmd${m.id}_nd</output>
    </summer>`;
  }).join('\n\n');

  const extProps = ms.map(m => `  <property value="0.0">fcs/cmd${m.id}_ext_nd</property>`).join('\n');

  const extSummers = ms.map(m => `    <summer>
      <input>fcs/cmd${m.id}_nd</input>
      <input>fcs/cmd${m.id}_ext_nd</input>
      <output>fcs/cmdEsc${m.id}_nd</output>
    </summer>`).join('\n');

  const tail = frame.tailRotor ? `
  <channel name="Tail Yaw Rotor">
    <!-- Bias keeps the (one-directional) tail rotor mid-range so the yaw
         PID can command moments in both directions; blowing right yaws the
         nose left, hence the negative gain. -->
    <pure_gain name="cmdYaw_2_tail">
      <input>fcs/cmdYaw_rps</input>
      <gain>${frame.tailRotor.gain}</gain>
      <output>fcs/cmdYawTail_nd</output>
    </pure_gain>
    <summer name="cmdEscTail">
      <input>fcs/cmdYawTail_nd</input>
      <bias>${frame.tailRotor.bias}</bias>
      <output>fcs/cmdEscTail_nd</output>
    </summer>
  </channel>
` : '';

  return `<?xml version="1.0" ?>
<flight_control name="FCS: ${frame.name}">
  <channel name="Pilot_Inputs">
    <summer name="pilotRoll_norm">
      <input>fcs/aileron-cmd-norm</input>
      <input>fcs/roll-trim-cmd-norm</input>
      <clipto>
        <min>-1.0</min>
        <max>1.0</max>
      </clipto>
      <output>fcs/pilotRoll_norm</output>
    </summer>
    <summer name="pilotPitch_norm">
      <input>fcs/elevator-cmd-norm</input>
      <input>fcs/pitch-trim-cmd-norm</input>
      <clipto>
        <min>-1.0</min>
        <max>1.0</max>
      </clipto>
      <output>fcs/pilotPitch_norm</output>
    </summer>
    <summer name="pilotYaw_norm">
      <input>fcs/rudder-cmd-norm</input>
      <input>fcs/yaw-trim-cmd-norm</input>
      <clipto>
        <min>-1.0</min>
        <max>1.0</max>
      </clipto>
      <output>fcs/pilotYaw_norm</output>
    </summer>

    <pure_gain name="refRoll_rps">
      <input>fcs/pilotRoll_norm</input>
      <gain>0.75</gain>
      <output>fcs/refRoll_rps</output>
    </pure_gain>
    <pure_gain name="refPitch_rps">
      <input>fcs/pilotPitch_norm</input>
      <gain>0.75</gain>
      <output>fcs/refPitch_rps</output>
    </pure_gain>
    <pure_gain name="refYaw_rps">
      <input>fcs/pilotYaw_norm</input>
      <gain>0.75</gain>
      <output>fcs/refYaw_rps</output>
    </pure_gain>
  </channel>

  <channel name="InnerLoop-Rates">
    <summer>
      <input>fcs/refRoll_rps</input>
      <input>-velocities/pi-rad_sec</input>
      <output>fcs/errRoll_rps</output>
    </summer>
    <pid name="fcs/cmdRoll_rps">
      <input>fcs/errRoll_rps</input>
      <kp>0.150</kp>
      <ki>0.050</ki>
      <kd>0.003</kd>
    </pid>

    <summer>
      <input>fcs/refPitch_rps</input>
      <input>-velocities/qi-rad_sec</input>
      <output>fcs/errPitch_rps</output>
    </summer>
    <pid name="fcs/cmdPitch_rps">
      <input>fcs/errPitch_rps</input>
      <kp>0.150</kp>
      <ki>0.050</ki>
      <kd>0.003</kd>
    </pid>

    <summer>
      <input>fcs/refYaw_rps</input>
      <input>-velocities/ri-rad_sec</input>
      <output>fcs/errYaw_rps</output>
    </summer>
    <pid name="fcs/cmdYaw_rps">
      <input>fcs/errYaw_rps</input>
      <kp>0.300</kp>
      <ki>0.100</ki>
      <kd>0.000</kd>
    </pid>
  </channel>

  <channel name="Control Mixer">
${mixer}
  </channel>
${tail}
${extProps}

  <channel name="External Input Summations">
${extSummers}
  </channel>
</flight_control>
`;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

const copied = ['Aero.xml', 'Metrics.xml', 'initGrnd.xml'];

for (const frame of FRAMES) {
  const dir = join(outRoot, frame.name);
  mkdirSync(dir, { recursive: true });
  writeFileSync(join(dir, `${frame.name}.xml`), mainXml(frame));
  writeFileSync(join(dir, 'Mass.xml'), massXml(frame));
  writeFileSync(join(dir, 'Gear.xml'), gearXml(frame));
  writeFileSync(join(dir, 'Propulsion.xml'), propulsionXml(frame));
  writeFileSync(join(dir, 'Effectors.xml'), effectorsXml(frame));
  writeFileSync(join(dir, 'FlightControl.xml'), flightControlXml(frame));
  for (const f of copied) {
    writeFileSync(join(dir, f), readFileSync(join(f450Dir, f), 'utf8'));
  }
  console.log(`generated ${frame.name} (${motors(frame).length} lift motors${frame.tailRotor ? ' + tail rotor' : ''}) -> ${dir}`);
}
